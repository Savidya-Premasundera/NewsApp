package fi.tuni.sd.newsapp.enums;

/**
 * Enumeration for article categories.
 * 
 * This enum represents various categories of news articles, allowing for 
 * structured and standardized categorization. It can be used to filter or 
 * classify articles by topic.
 * 
 * Categories include:
 * - GENERAL: General news.
 * - WORLD: International or global news.
 * - NATION: National or domestic news.
 * - BUSINESS: Business and economic news.
 * - TECHNOLOGY: Technology and innovation-related news.
 * - ENTERTAINMENT: Entertainment and cultural news.
 * - SPORTS: Sports-related news.
 * - SCIENCE: Science-related news.
 * - HEALTH: Health and medical news.
 * 
 * @author Savidya
 */
public enum ArticleCategoryEnum {
    
    GENERAL,
    WORLD,
    NATION,
    BUSINESS,
    TECHNOLOGY,
    ENTERTAINMENT,
    SPORTS,
    SCIENCE,
    HEALTH
    
}